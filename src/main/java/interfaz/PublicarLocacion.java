    private ImageIcon ResizeImage(String imgPath){
        int imageX = 177;
        int imageY = 202;
        fotoprevio.setSize(imageX, imageY);
        
        ImageIcon myImage = new ImageIcon(imgPath);
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(fotoprevio.getWidth(), fotoprevio.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }
    

    

