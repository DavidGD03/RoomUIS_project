private String imagePathStr;
    private void publicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_publicarActionPerformed
       if (!direccion.getText().equals("") && !precio.getText().equals("")) {
            String direccionv,extrav,detallesv,imagen;
            int nombrev;
            double preciov;
            nombrev = Integer.parseInt(nombre.getText());
            direccionv = direccion.getText();
            extrav = extra.getText();
            preciov = Double.parseDouble(precio.getText());
            detallesv = detalles.getText();
            imagen = imagePathStr;
       
            
            Boolean check = loCon.añadirLocacion(
                    nombrev,
                    direccionv,
                    extrav,
                    preciov,
                    detallesv,
                    imagen
            );

            if (check) {
                this.dispose();
                JOptionPane.showMessageDialog(null, "Has publicado la locación");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo publicar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos");
        }
    }//GEN-LAST:event_publicarActionPerformed

